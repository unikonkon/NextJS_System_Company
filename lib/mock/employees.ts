function computePayroll(
  emp: Employee,
  overtime: number,
  bonus: number,
): Payroll {
  const baseSalary = emp.salary ?? 0;
  const overtimePay = Math.round((baseSalary / 30 / 8) * overtime * 1.5);
  const gross = baseSalary + overtimePay + bonus;
  const socialSecurity = Math.min(Math.round(baseSalary * 0.05), 750);
  const taxable = gross - socialSecurity;
  const tax = Math.round(taxable * 0.05);
  const deductions = 0;
  const netSalary = gross - deductions - tax - socialSecurity;

  return {
    id: `pay-${emp.employeeCode}`,
    employeeId: emp.id,
    employeeName: `${emp.firstName} ${emp.lastName}`,
    employeeCode: emp.employeeCode,
    department: emp.department,
    position: emp.position,
    month: 'February',
    year: 2026,
    baseSalary,
    overtime: overtimePay,
    bonus,
    deductions,
    tax,
    socialSecurity,
    netSalary,
    status: 'APPROVED',
  };
}

